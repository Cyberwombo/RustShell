// Function to print a random banner with random color
pub fn print_random_banner() {
    let colors = [
        Color::Red,
        Color::Green,
        Color::Blue,
        Color::Cyan,
        Color::Magenta,
        Color::Yellow,
    ];

    let banner = random_banner();
    let color = colors.choose(&mut rng()).unwrap();

    println!("{}", banner.color(*color));
}